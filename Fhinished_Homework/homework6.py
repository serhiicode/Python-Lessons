x1 = int(input('Введіть координати коня х: '))
y1 = int(input('Введіть координати коня у: '))
x2 = int(input('Введіть координати хода х: '))
y2 = int(input('Введіть координати хода у: '))
d1 = x1 - x2
d2 = y1 - y2
if (d1 == 1 or d1 == -1) and (d2 == 2 or d2 == -2) or (d2 == 1 or d2 == -1) and (d1 == 2 or d1 == -2):
    print('Кінь може сюди ходити.')
else:
    print('Сюди коню ходити не можна.')